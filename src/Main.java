public class Main {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.print(i + " ");
            i++;
        }
        System.out.println("");
        for (int a = 10; a >= 1; a--) {
            System.out.print(a + " ");
        }
    }
}